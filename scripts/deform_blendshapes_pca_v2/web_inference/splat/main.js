    /**
     * KNN-based outlier filter: zeros opacity for Gaussians whose average
     * distance to K nearest neighbors exceeds `threshold`.
     * Uses a spatial hash grid for O(N) expected time.
     */
    function applyKnnFilter(baseData, numGaussians, k, threshold) {
        const t0 = performance.now();

        // Extract positions (offsets 0,1,2 in each 16-float block)
        const px = new Float32Array(numGaussians);
        const py = new Float32Array(numGaussians);
        const pz = new Float32Array(numGaussians);
        let minX = Infinity, minY = Infinity, minZ = Infinity;
        let maxX = -Infinity, maxY = -Infinity, maxZ = -Infinity;

        for (let g = 0; g < numGaussians; g++) {
            const off = g * 16;
            const x = baseData[off], y = baseData[off + 1], z = baseData[off + 2];
            px[g] = x; py[g] = y; pz[g] = z;
            if (x < minX) minX = x; if (x > maxX) maxX = x;
            if (y < minY) minY = y; if (y > maxY) maxY = y;
            if (z < minZ) minZ = z; if (z > maxZ) maxZ = z;
        }

        // Build spatial hash grid (cell size = threshold)
        const cs = threshold;
        const gW = Math.ceil((maxX - minX) / cs) + 1;
        const gH = Math.ceil((maxY - minY) / cs) + 1;
        const gD = Math.ceil((maxZ - minZ) / cs) + 1;

        const grid = new Map();
        for (let g = 0; g < numGaussians; g++) {
            const ci = Math.floor((px[g] - minX) / cs);
            const cj = Math.floor((py[g] - minY) / cs);
            const ck = Math.floor((pz[g] - minZ) / cs);
            const key = (ci * gH + cj) * gD + ck;
            let list = grid.get(key);
            if (!list) { list = []; grid.set(key, list); }
            list.push(g);
        }

        // For each Gaussian, find K nearest in ±1 cell neighborhood (3x3x3 = 27 cells)
        let filtered = 0;
        for (let g = 0; g < numGaussians; g++) {
            const x = px[g], y = py[g], z = pz[g];
            const ci = Math.floor((x - minX) / cs);
            const cj = Math.floor((y - minY) / cs);
            const ck = Math.floor((z - minZ) / cs);

            // Maintain K smallest distances
            const best = new Float32Array(k).fill(Infinity);

            for (let di = -1; di <= 1; di++) {
                const ni = ci + di;
                if (ni < 0 || ni >= gW) continue;
                for (let dj = -1; dj <= 1; dj++) {
                    const nj = cj + dj;
                    if (nj < 0 || nj >= gH) continue;
                    for (let dk = -1; dk <= 1; dk++) {
                        const nk = ck + dk;
                        if (nk < 0 || nk >= gD) continue;
                        const key = (ni * gH + nj) * gD + nk;
                        const list = grid.get(key);
                        if (!list) continue;
                        for (let li = 0; li < list.length; li++) {
                            const other = list[li];
                            if (other === g) continue;
                            const dx = px[other] - x;
                            const dy = py[other] - y;
                            const dz = pz[other] - z;
                            const d = Math.sqrt(dx * dx + dy * dy + dz * dz);
                            // Insert into best-K if smaller than current max
                            if (d < best[k - 1]) {
                                best[k - 1] = d;
                                // Bubble down to keep sorted
                                for (let i = k - 1; i > 0 && best[i] < best[i - 1]; i--) {
                                    const tmp = best[i]; best[i] = best[i - 1]; best[i - 1] = tmp;
                                }
                            }
                        }
                    }
                }
            }

            // Compute average distance to K nearest
            let avg = 0;
            let found = 0;
            for (let i = 0; i < k; i++) {
                if (best[i] < Infinity) { avg += best[i]; found++; }
            }

            const isOutlier = (found < k) || (avg / found > threshold);
            if (isOutlier) {
                baseData[g * 16 + 10] = -100.0;  // force opacity to 0 after clamp
                filtered++;
            }
        }

        const elapsed = (performance.now() - t0).toFixed(0);
        console.log(`KNN filter (K=${k}, threshold=${threshold}): removed ${filtered}/${numGaussians} in ${elapsed}ms`);
        return filtered;
    }

    // Fetch with progress tracking. Updates #loading-label and #loading-bar.
    // If url ends with .gz, decompresses with DecompressionStream after download.
    async function fetchWithProgress(url, label) {
        const resp = await fetch(url);
        if (!resp.ok) throw new Error(url + ': ' + resp.status);
        const totalBytes = +resp.headers.get('Content-Length');
        const isGz = url.endsWith('.gz');
        const labelEl = document.getElementById('loading-label');
        const barEl   = document.getElementById('loading-bar');

        if (!totalBytes || !resp.body) {
            if (labelEl) labelEl.textContent = label + '...';
            let buf = await resp.arrayBuffer();
            if (isGz) {
                if (labelEl) labelEl.textContent = label + ' decompressing...';
                buf = await new Response(new Blob([buf]).stream().pipeThrough(
                    new DecompressionStream('gzip'))).arrayBuffer();
            }
            return buf;
        }

        // Stream with progress
        const reader = resp.body.getReader();
        const chunks = [];
        let received = 0;
        while (true) {
            const { done, value } = await reader.read();
            if (done) break;
            chunks.push(value);
            received += value.length;
            const pct = Math.min(100, (received / totalBytes * 100) | 0);
            if (labelEl) labelEl.textContent = label + ' ' + pct + '%';
            if (barEl) barEl.style.width = pct + '%';
        }
        const raw = new Uint8Array(received);
        let offset = 0;
        for (const chunk of chunks) { raw.set(chunk, offset); offset += chunk.length; }

        if (isGz) {
            if (labelEl) labelEl.textContent = label + ' decompressing...';
            return await new Response(new Blob([raw]).stream().pipeThrough(
                new DecompressionStream('gzip'))).arrayBuffer();
        }
        return raw.buffer;
    }

    async function loadPCAAssets(seedDir) {
        // seedDir: per-identity dir (e.g. 'data/seed_110')
        // PCA basis is always loaded from 'data/' (shared across identities)
        const basisDir = 'data';
        if (!extColorFloat) {
            console.warn('loadPCAAssets: EXT_color_buffer_float unavailable, skipping.');
            return;
        }
        try {
            console.log('PCA: Loading identity from', seedDir, ', basis from', basisDir, '...');

            // 1. base_attributes.bin → RGBA32F base texture (per-identity)
            const baseBuf = await fetchWithProgress(seedDir + '/base_attributes.bin', 'Downloading base attributes');
            const baseHeader = new Uint32Array(baseBuf, 0, 4);
            const numGaussians = baseHeader[0];
            const baseTexW     = baseHeader[1];
            const baseTexH     = baseHeader[2];
            const baseData     = new Float32Array(baseBuf, 16);
            console.log(`PCA: base_attributes.bin  G=${numGaussians}  tex=${baseTexW}×${baseTexH}`);

            // Apply pre-upload filters (once, before GPU texture upload)
            applyKnnFilter(baseData, numGaussians, 8, 0.003);

            // Compute scale threshold for suppress_scale (percentile of max-scale-per-Gaussian).
            // Base attrs layout per Gaussian (16 floats = 4 RGBA32F texels):
            //   off+3 = scale_x, off+4 = scale_y, off+5 = scale_z  (physical, exp-activated)
            let scaleThreshold = 1e38;  // effectively Infinity → no culling
            if (suppressScaleVal > 0) {
                const maxScales = new Float32Array(numGaussians);
                for (let g = 0; g < numGaussians; g++) {
                    const off = g * 16;
                    maxScales[g] = (baseData[off + 3] + baseData[off + 4] + baseData[off + 5]) / 3.0;
                }
                const sorted = maxScales.slice().sort();
                const idx = Math.max(0, Math.min(numGaussians - 1,
                    Math.floor((suppressScaleVal / 100.0) * numGaussians)));
                scaleThreshold = sorted[idx];
                console.log(`PCA: suppress_scale=${suppressScaleVal}%  scale_threshold=${scaleThreshold.toFixed(5)}  (culling smallest ${suppressScaleVal}%)`);
            }

            // Compute centroid of Gaussian positions (offsets 0,1,2 of each 16-float block).
            let cx = 0, cy = 0, cz = 0;
            for (let g = 0; g < numGaussians; g++) {
                const off = g * 16;
                cx += baseData[off + 0];
                cy += baseData[off + 1];
                cz += baseData[off + 2];
            }
            const centroid = [cx / numGaussians, cy / numGaussians, cz / numGaussians];
            console.log(`PCA: centroid=[${centroid.map(v => v.toFixed(4)).join(', ')}]`);

            const baseTexture = gl.createTexture();
            gl.bindTexture(gl.TEXTURE_2D, baseTexture);
            gl.texImage2D(gl.TEXTURE_2D, 0, gl.RGBA32F, baseTexW, baseTexH, 0, gl.RGBA, gl.FLOAT, baseData);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.NEAREST);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MAG_FILTER, gl.NEAREST);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_S, gl.CLAMP_TO_EDGE);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_T, gl.CLAMP_TO_EDGE);

            // 2. pca_basis_K{k}.bin → RGBA16F basis texture (shared, from basisDir; try .gz first)
            const kParam   = new URLSearchParams(location.search).get('k') || '32';
            let basisBuf;
            try {
                basisBuf = await fetchWithProgress(basisDir + '/pca_basis_K' + kParam + '.bin.gz', 'Downloading PCA basis');
            } catch (e) {
                console.log('PCA: .gz not available, falling back to .bin');
                basisBuf = await fetchWithProgress(basisDir + '/pca_basis_K' + kParam + '.bin', 'Downloading PCA basis');
            }
            const basisHeader = new Uint32Array(basisBuf, 0, 4);
            const K          = basisHeader[0];
            const basisTexW  = basisHeader[2];
            const basisTexH  = basisHeader[3];
            const basisData  = new Uint16Array(basisBuf, 16);   // float16 bits as uint16
            console.log(`PCA: pca_basis_K${kParam}.bin  K=${K}  tex=${basisTexW}×${basisTexH}`);

            const maxTexSize = gl.getParameter(gl.MAX_TEXTURE_SIZE);
            if (basisTexW > maxTexSize || basisTexH > maxTexSize) {
                const msg = `PCA basis texture ${basisTexW}×${basisTexH} exceeds GPU MAX_TEXTURE_SIZE=${maxTexSize}. Try ?k=32 or ?k=16.`;
                console.error(msg);
                alert(msg);
                return;
            }

            const basisTexture = gl.createTexture();
            gl.bindTexture(gl.TEXTURE_2D, basisTexture);
            gl.texImage2D(gl.TEXTURE_2D, 0, gl.RGBA16F, basisTexW, basisTexH, 0, gl.RGBA, gl.HALF_FLOAT, basisData);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.NEAREST);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MAG_FILTER, gl.NEAREST);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_S, gl.CLAMP_TO_EDGE);
            gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_T, gl.CLAMP_TO_EDGE);

            // 3. mlp_weights.json → MLP object + w_code
            const labelEl = document.getElementById('loading-label');
            if (labelEl) labelEl.textContent = 'Loading MLP weights...';
            const mlpResp = await fetch(seedDir + '/mlp_weights.json');
            if (!mlpResp.ok) throw new Error('mlp_weights.json: ' + mlpResp.status);
            const mlpJson = await mlpResp.json();
            const mlp = new MLP(mlpJson);
            const wCode = new Float32Array(mlpJson.w_code);
            console.log(`PCA: mlp_weights.json  K=${mlp.K}  layers=${mlp.layers.length}  w_code=[${wCode.length}]`);

            // 4. driving_sequence.json → per-frame FLAME-55 inputs (always from data/ root)
            if (labelEl) labelEl.textContent = 'Loading driving sequence...';
            const seqResp = await fetch('data/driving_sequence.json');
            if (!seqResp.ok) throw new Error('driving_sequence.json: ' + seqResp.status);
            const seqJson     = await seqResp.json();
            const drivingFrames = seqJson.frames.map(f => new Float32Array(f));
            console.log(`PCA: driving_sequence.json  ${drivingFrames.length} frames @ ${seqJson.fps} fps`);

            // 5. MRT framebuffer: two RGBA32UI output textures
            if (labelEl) labelEl.textContent = 'Setting up textures...';
            const barEl = document.getElementById('loading-bar');
            if (barEl) barEl.style.width = '100%';
            const outTexW = 1024;
            const outTexH = Math.ceil(numGaussians / 1024);
            const posTexture = createOutputTexture(outTexW, outTexH);
            const covTexture = createOutputTexture(outTexW, outTexH);

            const computeFBO = gl.createFramebuffer();
            gl.bindFramebuffer(gl.FRAMEBUFFER, computeFBO);
            gl.framebufferTexture2D(gl.FRAMEBUFFER, gl.COLOR_ATTACHMENT0, gl.TEXTURE_2D, posTexture, 0);
            gl.framebufferTexture2D(gl.FRAMEBUFFER, gl.COLOR_ATTACHMENT1, gl.TEXTURE_2D, covTexture, 0);
            const fboStatus = gl.checkFramebufferStatus(gl.FRAMEBUFFER);
            gl.bindFramebuffer(gl.FRAMEBUFFER, null);
            if (fboStatus !== gl.FRAMEBUFFER_COMPLETE) {
                throw new Error('MRT framebuffer incomplete: 0x' + fboStatus.toString(16));
            }

            // 6. Upload static uniforms for computeProgram
            gl.useProgram(computeProgram);
            gl.uniform1i(u_compute_K,      K);
            gl.uniform1i(u_compute_numG,   numGaussians);
            gl.uniform1i(u_compute_basisW, basisTexW);
            gl.uniform1i(u_compute_baseW,  baseTexW);
            gl.uniform1f(u_compute_scaleThresh, scaleThreshold);
            gl.useProgram(program);

            // 7. camera_params.json → set initial view for the face scene
            try {
                const camResp = await fetch(seedDir + '/camera_params.json');
                if (camResp.ok) {
                    const camParams = await camResp.json();
                    camera = camParams;
                    const faceViewMatrix = getViewMatrix(camParams);
                    defaultViewMatrix = faceViewMatrix;
                    viewMatrix = faceViewMatrix;
                    pullBackCamera(centroid);
                    resize();
                    console.log('PCA: camera_params.json loaded, view updated!');
                } else {
                    console.warn('PCA: camera_params.json not found, keeping default camera.');
                }
            } catch (camErr) {
                console.warn('PCA: camera_params.json load failed:', camErr);
            }

            pcaAssets = {
                numGaussians, K, mlp, wCode, drivingFrames,
                drivingFps: seqJson.fps,
                baseTexture, basisTexture,
                posTexture, covTexture, computeFBO,
                outTexW, outTexH, baseTexW, basisTexW,
                centroid,
            };
            console.log('PCA: All assets loaded. Inference active.');
            const overlay = document.getElementById('loading-overlay');
            if (overlay) overlay.style.display = 'none';

        } catch (err) {
            console.error('loadPCAAssets failed:', err);
        }
    }

