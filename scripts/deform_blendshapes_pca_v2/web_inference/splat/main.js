// Compute pass fragment shader: reads base attributes + PCA basis from textures,
// accumulates PCA deltas weighted by MLP coefficients, applies geometric constraints,
// computes Gaussian covariance from quaternion + scale, and writes to two MRT outputs:
//   COLOR_ATTACHMENT0 (posTexture)  — packed position as RGBA32UI
//   COLOR_ATTACHMENT1 (covTexture)  — packed covariance + color as RGBA32UI
const computeFragmentShaderSource = `
#version 300 es
precision highp float;
precision highp int;

uniform sampler2D u_base;        // RGBA32F,  4 texels per Gaussian
uniform sampler2D u_basis;       // RGBA16F,  K*G*3 texels (k outer, g middle, sub-texel inner)
uniform vec4 u_coeffs[64];       // coeffs[k] = vec4(c_xyz, c_scale, c_rot, c_opa)
uniform int u_K;
uniform int u_num_gaussians;
uniform int u_basis_tex_width;
uniform int u_base_tex_width;
uniform float u_opacity_threshold;
uniform float u_scale_threshold;

layout(location = 0) out uvec4 outPosition;
layout(location = 1) out uvec4 outCovariance;

void main() {
    int g = int(gl_FragCoord.y) * 1024 + int(gl_FragCoord.x);
    if (g >= u_num_gaussians) {
        outPosition   = uvec4(0u);
        outCovariance = uvec4(0u);
        return;
    }

    // ── Read base attributes (4 RGBA32F texels per Gaussian) ─────────────────
    int base_idx = g * 4;
    vec4 b0 = texelFetch(u_base, ivec2(base_idx       % u_base_tex_width, base_idx       / u_base_tex_width), 0);
    vec4 b1 = texelFetch(u_base, ivec2((base_idx + 1) % u_base_tex_width, (base_idx + 1) / u_base_tex_width), 0);
    vec4 b2 = texelFetch(u_base, ivec2((base_idx + 2) % u_base_tex_width, (base_idx + 2) / u_base_tex_width), 0);
    vec4 b3 = texelFetch(u_base, ivec2((base_idx + 3) % u_base_tex_width, (base_idx + 3) / u_base_tex_width), 0);

    // Texel 0: [pos_x,   pos_y,   pos_z,   scale_x]
    // Texel 1: [scale_y, scale_z, rot_w,   rot_x  ]
    // Texel 2: [rot_y,   rot_z,   opacity, color_R]
    // Texel 3: [color_G, color_B, 0.0,     0.0    ]
    vec3  pos   = b0.xyz;
    vec3  scale = vec3(b0.w, b1.x, b1.y);
    vec4  rot   = vec4(b1.z, b1.w, b2.x, b2.y);   // [w, x, y, z]
    float opa   = b2.z;
    vec3  baseColor = vec3(b2.w, b3.x, b3.y);      // RGB in [0, 1]

    // ── Accumulate PCA deltas ─────────────────────────────────────────────────
    vec3  d_pos = vec3(0.0);
    vec3  d_scl = vec3(0.0);
    vec4  d_rot = vec4(0.0);
    float d_opa = 0.0;

    for (int k = 0; k < u_K; k++) {
        int basis_idx = k * u_num_gaussians * 3 + g * 3;
        vec4 v0 = texelFetch(u_basis, ivec2(basis_idx       % u_basis_tex_width, basis_idx       / u_basis_tex_width), 0);
        vec4 v1 = texelFetch(u_basis, ivec2((basis_idx + 1) % u_basis_tex_width, (basis_idx + 1) / u_basis_tex_width), 0);
        vec4 v2 = texelFetch(u_basis, ivec2((basis_idx + 2) % u_basis_tex_width, (basis_idx + 2) / u_basis_tex_width), 0);

        // v0: [d_pos_x,   d_pos_y,   d_pos_z,  d_scale_x]
        // v1: [d_scale_y, d_scale_z, d_rot_w,  d_rot_x  ]
        // v2: [d_rot_y,   d_rot_z,   d_opacity, 0.0      ]
        vec4 c = u_coeffs[k];
        d_pos += c.x * v0.xyz;
        d_scl += c.y * vec3(v0.w, v1.x, v1.y);
        d_rot += c.z * vec4(v1.z, v1.w, v2.x, v2.y);
        d_opa += c.w * v2.z;
    }

    // ── Apply geometric constraints ───────────────────────────────────────────
    pos   += d_pos;
    scale  = max(scale + d_scl, vec3(1e-5));
    if ((scale.x + scale.y + scale.z) / 3.0 < u_scale_threshold) {
        outPosition   = uvec4(0u);
        outCovariance = uvec4(0u);
        return;
    }
    rot    = normalize(rot + d_rot);
    opa    = clamp(opa + d_opa, 0.0, 1.0);

    if (opa < u_opacity_threshold) {
        outPosition   = uvec4(0u);
        outCovariance = uvec4(0u);
        return;
    }

    // ── Output 0: Position (3 floats packed as uint32 bit patterns) ──────────
    outPosition = uvec4(
        floatBitsToUint(pos.x),
        floatBitsToUint(pos.y),
        floatBitsToUint(pos.z),
        0u
    );

    // ── Output 1: Covariance + color ─────────────────────────────────────────
    // Build rotation matrix from quaternion [w, x, y, z] stored as rot = (w,x,y,z)
    float rw = rot.x, rx = rot.y, ry = rot.z, rz = rot.w;
    // GLSL mat3 is column-major: mat3(col0, col1, col2)
    mat3 R = mat3(
        1.0 - 2.0*(ry*ry + rz*rz),  2.0*(rx*ry + rw*rz),         2.0*(rx*rz - rw*ry),
        2.0*(rx*ry - rw*rz),         1.0 - 2.0*(rx*rx + rz*rz),  2.0*(ry*rz + rw*rx),
        2.0*(rx*rz + rw*ry),         2.0*(ry*rz - rw*rx),         1.0 - 2.0*(rx*rx + ry*ry)
    );
    // M = R * diag(scale):  scale each column of R by corresponding scale value
    mat3 M = R * mat3(scale.x, 0.0, 0.0,  0.0, scale.y, 0.0,  0.0, 0.0, scale.z);
    // Covariance sigma = M * M^T
    mat3 S = M * transpose(M);

    // Pack covariance in the same layout as the web worker's generateTexture():
    //   outCovariance.x = packHalf2x16(4*sigma00, 4*sigma01)
    //   outCovariance.y = packHalf2x16(4*sigma02, 4*sigma11)
    //   outCovariance.z = packHalf2x16(4*sigma12, 4*sigma22)
    //   outCovariance.w = colorPacked with updated alpha
    // GLSL: S[col][row], so S[0][0]=sigma00, S[1][0]=sigma01, S[2][0]=sigma02, etc.
    uint rr = uint(clamp(baseColor.r * 255.0, 0.0, 255.0));
    uint gg = uint(clamp(baseColor.g * 255.0, 0.0, 255.0));
    uint bb = uint(clamp(baseColor.b * 255.0, 0.0, 255.0));
    uint alpha = uint(clamp(opa * 255.0, 0.0, 255.0));
    uint colorWithAlpha = rr | (gg << 8u) | (bb << 16u) | (alpha << 24u);

    outCovariance = uvec4(
        packHalf2x16(vec2(4.0 * S[0][0], 4.0 * S[1][0])),
        packHalf2x16(vec2(4.0 * S[2][0], 4.0 * S[1][1])),
        packHalf2x16(vec2(4.0 * S[2][1], 4.0 * S[2][2])),
        colorWithAlpha
    );
}
`.trim();

