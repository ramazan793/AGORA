class MLP {
    constructor(json) {
        this.K     = json.K;
        this.alpha = json.alpha || 0.2;
        this.layers = json.layers.map(l => ({
            weight: new Float32Array(l.weight.flat()),  // row-major [out × in]
            bias:   new Float32Array(l.bias),
            inDim:  l.in,
            outDim: l.out,
        }));
    }

    // input: Float32Array(567)  →  Float32Array(4*K)  (w_code[512] + flame55[55])
    forward(input) {
        let h = input;
        for (let i = 0; i < this.layers.length; i++) {
            const { weight, bias, outDim, inDim } = this.layers[i];
            const out = new Float32Array(outDim);
            for (let o = 0; o < outDim; o++) {
                let sum = bias[o];
                const row_off = o * inDim;
                for (let j = 0; j < inDim; j++) sum += weight[row_off + j] * h[j];
                out[o] = sum;
            }
            h = out;
            if (i < this.layers.length - 1) {     // LeakyReLU, not after last layer
                for (let j = 0; j < h.length; j++) if (h[j] < 0) h[j] *= this.alpha;
            }
        }
        return h;
    }
}

// Run full deformation network via ONNX Runtime Web.
// The model has 3 inputs:
//   deformation_ws: [1, 55]         — FLAME expression params
//   feature_maps:   [1, 512, 64, 64] — backbone triplane features (dummy for latency benchmark)
//   ws_main:        [1, 5, 512]      — main branch W-space codes  (dummy for latency benchmark)
async function run_full_deformation_network(session, flame55) {
    const feeds = {
        deformation_ws: new ort.Tensor('float32', flame55, [1, 55]),
        feature_maps:   new ort.Tensor('float32', new Float32Array(1 * 512 * 64 * 64), [1, 512, 64, 64]),
        ws_main:        new ort.Tensor('float32', new Float32Array(1 * 5 * 512),        [1, 5, 512]),
    };
    const results = await session.run(feeds);
    return results;
}

